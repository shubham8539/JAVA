public class bitwise {
    public static void main(String[] args) {
        System.out.println(2&3);
        System.out.println(2|3);
        System.out.println(~2);
        System.out.println(7<<2);
        System.out.println(7>>2);
        System.out.println(-4<<3);
        System.out.println(-4>>2);



    }
    
}
